package ArrayDemo;

public class TwoDarrayDemo {

	// This is called 2D Array....
	public static void main(String[] args) {
		
		// There is declare as 2 row and 2 values....
		
		int school[][]=new int [2][2];
		
		school[0][0]=12;
		school[0][1]=24;
		school[1][0]=36;
		school[1][1]=48;
		
		System.out.println(school[1][1]);
	}
}
